#include "3-calc.h"

/**
 * op_add - return sum
 * @a:first operand
 * @b:second operand
 * Return: always 0
 */

int op_add (int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return difference
 * @a: first operand
 * @b: second operand
 * Return: always 0
 */

int op_sub (int a, int b)
{
	return (a - b);
}

/**
 * op_mul -return the product
 * @a: first operand
 * @b: second operand
 * Return: always 0
 */

int op_mul (int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: first operand
 * @b: second operand
 * Return: always 0
 */

int op_div (int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the reminder
 * @a: first operand
 * @b: second operand
 * Return: always 0
 */

int op_mod (int a, int b)
{
	return (a % b);
}
